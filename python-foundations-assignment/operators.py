## Simple Calculator

x = 3
y = 4.5
print(x + y)  # Output: 7.5
print(y - x)  # Output: 1.5
print(x * y)  # Output: 13.5
print(y / x)  # Output: 1.5
print(x ** 2)  # Output: 9


## Area of Shapes


# Area of rectangle

Length = 12
Width = 8
Area = Length * Width
print("The area of the rectangle is:", Area)

# Area of a circle

radius = 35
Area_Circle = 3.14159 * radius ** 2
print("The area of the circle is:", Area_Circle)

# Area of a triangle
base = 7
height = 10
Area_Triangle = 0.5 * base * height
print("The area of the triangle is:", Area_Triangle)

